def parse_fgi(raw: str):
    result = []
    for line in raw.strip().split("\n"):
        line = line.strip()
        if not line:
            continue
        parts = line.split(",")
        if len(parts) == 2:
            try:
                result.append((parts[0].strip(), float(parts[1].strip())))
            except ValueError:
                continue
    return result


def get_zone(score: float) -> str:
    if score < THRESHOLDS["extreme_fear"]:
        return "extreme_fear"
    elif score < THRESHOLDS["fear"]:
        return "fear"
    elif score <= THRESHOLDS["neutral"]:
        return "neutral"
    elif score <= THRESHOLDS["greed"]:
        return "greed"
    else:
        return "extreme_greed"


