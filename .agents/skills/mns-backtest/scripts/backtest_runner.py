def analyze_backtest(state: BacktestState):
    if not state.daily_values:
        return None

    first = state.daily_values[0]
    last = state.daily_values[-1]
    start_date, end_date = first["date"], last["date"]
    years = (datetime.strptime(end_date, "%Y-%m-%d") -
             datetime.strptime(start_date, "%Y-%m-%d")).days / 365.25

    final_value = last["total_value"]
    total_inflow = state.total_inflow
    total_profit = final_value - total_inflow
    total_return_pct = (total_profit / total_inflow) * 100 if total_inflow > 0 else 0
    annualized = ((final_value / total_inflow) ** (1 / years) - 1) * 100 if years > 0 and total_inflow > 0 else 0

    buy_trades = [t for t in state.trades if t.get("action") == "买入"]
    sell_trades = [t for t in state.trades if t.get("action") == "卖出"]

    peak, max_dd = 0, 0
    for dv in state.daily_values:
        tv = dv["total_value"]
        if tv > peak:
            peak = tv
        dd = (peak - tv) / peak if peak > 0 else 0
        if dd > max_dd:
            max_dd = dd

    print(f"\n{'='*65}")
    print(f"   逆向策略 回测报告")
    print(f"   回测区间: {start_date} ~ {end_date} ({years:.1f} 年)")
    print(f"{'='*65}")

    print(f"\n  【收益概览】")
    print(f"    总投入资金:     ¥{total_inflow:>12,.2f}")
    print(f"    期末总资产:     ¥{final_value:>12,.2f}")
    print(f"    总收益:         ¥{total_profit:>12,.2f}")
    print(f"    总收益率:       {total_return_pct:>11.2f}%")
    print(f"    年化收益率:     {annualized:>11.2f}%")
    print(f"    最大回撤:       {max_dd*100:>11.2f}%")

    print(f"\n  【交易统计】")
    print(f"    买入次数: {len(buy_trades):>4}  |  卖出次数: {len(sell_trades):>4}")

    print(f"\n  【按情绪区间 - 买入】")
    zone_buy = {}
    for t in buy_trades:
        z = t.get("zone", "unknown")
        zone_buy.setdefault(z, {"count": 0, "amount": 0})
        zone_buy[z]["count"] += 1
        zone_buy[z]["amount"] += t.get("amount", 0)
    for z in ["extreme_fear", "fear", "neutral"]:
        if z in zone_buy:
            s = zone_buy[z]
            print(f"    {ZONE_CN.get(z,z):>6}: {s['count']:>3} 次, ¥{s['amount']:>10,.2f}")

    print(f"\n  【按情绪区间 - 卖出】")
    zone_sell = {}
    for t in sell_trades:
        z = t.get("zone", "unknown")
        zone_sell.setdefault(z, {"count": 0, "amount": 0})
        zone_sell[z]["count"] += 1
        zone_sell[z]["amount"] += t.get("amount", 0)
    for z in ["neutral", "greed", "extreme_greed"]:
        if z in zone_sell:
            s = zone_sell[z]
            print(f"    {ZONE_CN.get(z,z):>6}: {s['count']:>3} 次, ¥{s['amount']:>10,.2f}")

    print(f"\n  【关键交易】（每年 Top 3）")
    by_year = {}
    for t in state.trades:
        if t.get("action") in ("买入", "卖出"):
            by_year.setdefault(t["date"][:4], []).append(t)
    for yr in sorted(by_year.keys()):
        for t in sorted(by_year[yr], key=lambda x: x.get("amount", 0), reverse=True)[:3]:
            ann = f", 年化{t.get('ann_ret','')}%" if t.get("ann_ret") else ""
            print(f"    {t['date']} {t['action']} {ZONE_CN.get(t.get('zone',''),'')}"
                  f"(FGI:{t.get('fgi','')}), ¥{t.get('amount',0):,.0f} "
                  f"({t.get('buy_pct','') or t.get('sell_pct','')}"
                  f"{ann})")

    return {
        "label": "逆向策略", "annualized": annualized,
        "total_return_pct": total_return_pct, "max_drawdown": max_dd * 100,
        "buy_count": len(buy_trades), "sell_count": len(sell_trades),
    }


