@dataclass
class Position:
    shares: float = 0.0
    cost_price: float = 0.0
    first_buy_date: str = ""

    def current_value(self, price: float) -> float:
        return self.shares * price

    def annualized_return(self, price: float, current_date: str) -> float:
        if self.shares <= 0 or self.cost_price <= 0 or not self.first_buy_date:
            return 0.0
        days = (datetime.strptime(current_date, "%Y-%m-%d") -
                datetime.strptime(self.first_buy_date, "%Y-%m-%d")).days
        if days <= 0:
            return 0.0
        ratio = price / self.cost_price
        return (ratio ** (365.0 / days) - 1) * 100


@dataclass
class BacktestState:
    cash: float = INITIAL_CASH
    position: Position = field(default_factory=Position)
    total_inflow: float = INITIAL_CASH
    trades: list = field(default_factory=list)
    daily_values: list = field(default_factory=list)

    def total_value(self, price: float) -> float:
        return self.cash + self.position.current_value(price)


def run_backtest(fg_data):
    state = BacktestState()
    last_inflow_year = 0
    prev_zone = None

    for date_str, score in fg_data:
        price = get_sp500_price(date_str)
        if price is None:
            continue

        current_year = int(date_str[:4])
        current_month = int(date_str[5:7])
        if current_year > last_inflow_year and current_month >= 2:
            state.cash += ANNUAL_INFLOW
            state.total_inflow += ANNUAL_INFLOW
            last_inflow_year = current_year
            state.trades.append({"date": date_str, "action": "年度注资",
                                 "amount": ANNUAL_INFLOW, "cash_after": state.cash})

        zone = get_zone(score)
        zone_changed = (zone != prev_zone)
        prev_zone = zone

        # ---- 卖出 ----
        if zone in ("extreme_greed", "greed", "neutral") and zone_changed:
            pos = state.position
            if pos.shares > 0:
                ann_ret = pos.annualized_return(price, date_str)
                ret_zone = "above_high" if ann_ret >= ANNUALIZED_TARGET_HIGH else \
                           "between" if ann_ret >= ANNUALIZED_TARGET_LOW else "below_low"
                sell_pct = SELL_RATIO.get((zone, ret_zone), 0.0)
                if sell_pct > 0:
                    sell_shares = pos.shares * sell_pct
                    sell_amount = sell_shares * price
                    if sell_shares >= 0.01:
                        pos.shares -= sell_shares
                        state.cash += sell_amount
                        if pos.shares < 0.01:
                            pos.shares = 0
                            pos.cost_price = 0
                            pos.first_buy_date = ""
                        state.trades.append({
                            "date": date_str, "action": "卖出", "zone": zone, "fgi": score,
                            "shares": round(sell_shares, 2), "price": round(price, 2),
                            "amount": round(sell_amount, 2), "ann_ret": round(ann_ret, 1),
                            "sell_pct": f"{sell_pct*100:.0f}%", "cash_after": round(state.cash, 2)
                        })

        # ---- 买入 ----
        if zone in ("extreme_fear", "fear", "neutral") and zone_changed:
            buy_pct = BUY_RATIO.get(zone, 0.0)
            invest_amount = state.cash * buy_pct
            if invest_amount > 0 and price > 0:
                buy_shares = invest_amount / price
                if buy_shares >= 0.01:
                    pos = state.position
                    if pos.shares == 0:
                        pos.first_buy_date = date_str
                        pos.cost_price = price
                    else:
                        total_shares = pos.shares + buy_shares
                        pos.cost_price = (pos.shares * pos.cost_price + buy_shares * price) / total_shares
                    pos.shares += buy_shares
                    state.cash -= invest_amount
                    state.trades.append({
                        "date": date_str, "action": "买入", "zone": zone, "fgi": score,
                        "shares": round(buy_shares, 2), "price": round(price, 2),
                        "amount": round(invest_amount, 2), "buy_pct": f"{buy_pct*100:.0f}%",
                        "cash_after": round(state.cash, 2)
                    })

        state.daily_values.append({
            "date": date_str, "fgi": score, "zone": zone,
            "sp500": price, "cash": round(state.cash, 2),
            "position_value": round(state.position.current_value(price), 2),
            "total_value": round(state.total_value(price), 2),
        })

    return state


def run_buy_and_hold(fg_data):
    cash = INITIAL_CASH
    shares = 0.0
    total_inflow = INITIAL_CASH
    last_inflow_year = 0
    daily_values = []
    first_invested = False

    for date_str, score in fg_data:
        price = get_sp500_price(date_str)
        if price is None:
            continue

        current_year = int(date_str[:4])
        current_month = int(date_str[5:7])
        if current_year > last_inflow_year and current_month >= 2:
            shares += ANNUAL_INFLOW / price
            total_inflow += ANNUAL_INFLOW
            last_inflow_year = current_year

        if not first_invested and cash > 0:
            shares = cash / price
            cash = 0
            first_invested = True

        daily_values.append({"date": date_str, "total_value": round(shares * price, 2)})

    return daily_values, total_inflow


